# include "rt.h"

int			rt_warn(char *msg)
{
	ft_printf("Warning: %s", msg);
	return (0);
}

int			rt_warning(char *msg, int ecode)
{
	ft_printf("Warning: ($i) %s", ecode, msg);
	return (ecode);
}
